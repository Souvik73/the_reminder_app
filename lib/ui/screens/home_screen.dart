        ],
      ),
      child: Column(
        children: [
          _buildActivityItem(
            'Morning Workout Reminder',
            '2 hours ago',
            Icons.fitness_center,
            const Color(0xFF10B981),
          ),
          const Divider(height: 1),
          _buildActivityItem(
            'Team Meeting Started',
            '4 hours ago',
            Icons.video_call,
            const Color(0xFF3B82F6),
          ),
          const Divider(height: 1),
